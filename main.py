final = []
for word in token_words:
    if word not in useless_words:
        final.append(word)
print(final)
empty_emotion_list = []
with open('sentiments.txt', 'r') as file:
    for line in file:
        clear_line = line.replace("\n", '').replace(",", '').replace("'", '').strip()
        word, emotion = clear_line.split(':')

        if word in final:
            empty_emotion_list.append(emotion)



print(empty_emotion_list)

a = Counter(empty_emotion_list)
print(a)
fig, axis = plt.subplots()
axis.bar(a.keys(), a.values())
fig.autofmt_xdate()
